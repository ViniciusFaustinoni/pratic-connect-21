-- Criar tabela de consultores (sem login/senha)
CREATE TABLE public.consultores (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  nome VARCHAR(255) NOT NULL,
  email VARCHAR(255),
  telefone VARCHAR(20),
  ativo BOOLEAN DEFAULT true,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Índices para performance
CREATE INDEX idx_consultores_nome ON public.consultores(nome);
CREATE INDEX idx_consultores_ativo ON public.consultores(ativo);

-- Trigger para updated_at
CREATE TRIGGER update_consultores_updated_at
  BEFORE UPDATE ON public.consultores
  FOR EACH ROW
  EXECUTE FUNCTION public.update_updated_at_column();

-- Habilitar RLS
ALTER TABLE public.consultores ENABLE ROW LEVEL SECURITY;

-- Políticas RLS
CREATE POLICY "Todos podem visualizar consultores" 
  ON public.consultores FOR SELECT USING (true);

CREATE POLICY "Gerência pode inserir consultores" 
  ON public.consultores FOR INSERT 
  WITH CHECK (is_gerencia(auth.uid()));

CREATE POLICY "Gerência pode atualizar consultores" 
  ON public.consultores FOR UPDATE 
  USING (is_gerencia(auth.uid()));

CREATE POLICY "Gerência pode excluir consultores" 
  ON public.consultores FOR DELETE 
  USING (is_gerencia(auth.uid()));

-- Adicionar campo consultor_id na tabela leads
ALTER TABLE public.leads ADD COLUMN consultor_id UUID REFERENCES public.consultores(id);
CREATE INDEX idx_leads_consultor_id ON public.leads(consultor_id);

